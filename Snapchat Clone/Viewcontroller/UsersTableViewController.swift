        cell.textLabel?.text = listOfUsers[indexPath.row].nome
        cell.detailTextLabel?.text = listOfUsers[indexPath.row].email

        return cell
    }
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
